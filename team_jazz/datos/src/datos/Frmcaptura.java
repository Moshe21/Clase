    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Conectar cc= new Conectar();
        Connection cn=cc.conexion();
        String sql="";
        String cod,nom,ape;
        cod=txtcodigo.getText();

        sql="select * from tblclinetes where codigo ="+cod;
         try{
             PreparedStatement comando=cn.prepareStatement(sql);
             ResultSet registro=comando.executeQuery(sql);
             if (registro.next()== true)
                 {
                   JOptionPane.showMessageDialog(null,"El registro encontrado es: ");
                   txtnombre.setText(registro.getString("nombre"));
                   txtapellido.setText(registro.getString("apellido"));
                 }
          }catch(Exception e){
                     JOptionPane.showMessageDialog(null,"No se encontro El registro suministrado "+e);    
                 } 
                
        
            
    }//GEN-LAST:event_jButton2ActionPerformed

